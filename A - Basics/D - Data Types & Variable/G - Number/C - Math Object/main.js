console.log(Math.round(5.5)); // 6
console.log(Math.round(5.4)); // 5

console.log(Math.ceil(5.1));  // 6
console.log(Math.floor(5.9)); // 5

console.log(Math.max(100 , 202 , -10 , 9)); // 202
console.log(Math.min(100 , 202 , -10 , 9)); // -10

console.log(Math.pow(2,4)); // 16
console.log(Math.random()); // 0.07283176938389602

console.log(Math.trunc(99.90)); // 99 => return Integer value